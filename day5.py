#Learning Operators
print(15+6)
print(15-6)
print(15*6)
print(15/6) #(division)Decimal value of quotient is printed in this
print(15//6) #This is known as 'floor division'.Decimal value of quotient is not printed in this
print(15%6) #(Modulus)This only print a remainder
print(5**3) #(Exponential)This multiply 5 three times